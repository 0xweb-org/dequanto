import {
    FeeMarketEIP1559Transaction,
    Transaction,
    TxData,
    FeeMarketEIP1559TxData,
    TxOptions
} from '@ethereumjs/tx';

export namespace TxFactory {
    export function fromTxData (txData: TxData | FeeMarketEIP1559TxData, opts?: TxOptions) {
        const Factory = txData.type && Number(txData.type) === 2
            ? FeeMarketEIP1559Transaction
            : Transaction

        return Factory.fromTxData(txData as any, opts);
    }
}
